package main

import "fmt" 

func main() {
	// Integer is a variable that stores an integer value
	var integer = 10 

	// Float is a variable that stores a floating-point value
	var float = 12.5 

	// String is a variable that stores a text or string value
	var string = "Hello, World!" 

	// Boolean is a variable that stores a boolean value
	var boolean = true

	fmt.Println("Integer:", integer)
	fmt.Println("Float:", float)
	fmt.Println("String:", string)
	fmt.Println("Boolean:", boolean)
}

/*
	Output:
		Integer: 10
		Float: 12.5
		String: Hello, World!
		Boolean: true
*/