// Package declaration
package main

// Import fmt package
import "fmt" 

func main() {
	// Print without new line
	fmt.Print("Hello, World!\n"); 

	 // Print with new line
	fmt.Println("Hello, World!");

	// Print with formatted string
	fmt.Printf("Hello, %s!\n", "World"); 
}

/*
	Output:
		Hello, World!
		Hello, World!
		Hello, World!
*/